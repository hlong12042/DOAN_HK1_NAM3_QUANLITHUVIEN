    private void soLuongSachActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_soLuongSachActionPerformed
        // TODO add your handling code here:
        this.soLuong = Integer.parseInt(this.soLuongSach.getSelectedItem().toString());    
        if(this.soLuong != 0){
            String temp = dsCacSach.getSelectedValue().toString() +"-"+ Integer.toString(this.soLuong);
            for(int i = 0;i < dsMua.size();i++){
                if(dsMua.get(i).split("-")[0].equals(temp.split("-")[0])){
                    dsMua.remove(i);
                    break;
                }                 
            }
            this.dsMua.add(temp);           
        }
    }//GEN-LAST:event_soLuongSachActionPerformed

