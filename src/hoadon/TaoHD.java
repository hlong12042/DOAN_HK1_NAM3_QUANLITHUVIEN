    private void dsCacSachMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_dsCacSachMouseClicked
        this.soLuongSach.setVisible(true);
        String row = dsCacSach.getSelectedValue();
        String maSach = row.split("-")[0];
        for (String temp:dsMua){
            if(temp.split("-")[0].equals(maSach)){
                int value = Integer.parseInt(temp.split("-")[7]);
                this.soLuongSach.setSelectedItem(value);
                break;
            }
            else
                this.soLuongSach.setSelectedItem(0);
        }
                 
    }//GEN-LAST:event_dsCacSachMouseClicked

    private void btnXuatHDMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnXuatHDMouseClicked
        // TODO add your handling code here:
        int temp = dsMua.size();
        if(dsMua.size()==dsSachTrongKho.size()){
            long tien = updateTongTien(this.dsMua);
            LocalDateTime thoiGianXuat = LocalDateTime.now();
            HoaDon hd = new HoaDon(this.getMAHD(),this.getTenNV(),this.getDSMua(),thoiGianXuat,tien);
            HOADONFORM hdf = new HOADONFORM(hd.getTenNV(),hd.getIDHoaDon(),hd.getListIDSach(),hd.getTGXuat(),tien);
            hdf.setVisible(true);
            if(hdf.option()==1)
                this.dispose();
        }
        else{
            JOptionPane.showMessageDialog(rootPane, "Nhập thiếu số lượng!", "Warning!", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnXuatHDMouseClicked

