    public long updateTongTien(List<String> ds){ 
        long tien = 0;
        for(String item: this.dsMua){      
            tien += Long.parseLong(item.split("-")[4])* Long.parseLong(item.split("-")[7]);                                                                 
        }
        return tien;
    }
    
    public boolean ktDanhSach(String str){
        for(String s : this.dsMua){
            if(s == str){
                return true;
            }
        }        
        return false;
    }
    
    public void themDS(){
        String maSach = inputTimKiem.getText();
        if(maSach.isEmpty())
            LabelThongTin.setText("Khung tìm kiếm đang trống");
        if(!KTinput(maSach))
            JOptionPane.showMessageDialog(rootPane, "Không nhập kí tự đặt biệt", "Cảnh báo", JOptionPane.ERROR_MESSAGE);
        else{
            Sach.Kho kho = new Sach.Kho();
            Sach.Sach sach = kho.timMaSach(maSach);
            if(sach==null)
                LabelThongTin.setText("Mã sách không tồn tại");
            else{
                LabelThongTin.setText(sach.toString());
                dsSachTrongKho.add(sach.toString());
                dsCacSach.setListData((Vector<String>)dsSachTrongKho);
                dsCacSach.setSelectedIndex(dsSachTrongKho.size()-1);
            }
        }
    }
    
    public void timKiem(){
        String maSach = inputTimKiem.getText();
        if(!KTinput(maSach))
            JOptionPane.showMessageDialog(rootPane, "Không nhập kí tự đặt biệt", "Cảnh báo", JOptionPane.ERROR_MESSAGE);
        else{
            Sach.Kho kho = new Sach.Kho();
            Sach.Sach sach = kho.timMaSach(maSach);
            if(sach==null)
                LabelThongTin.setText("Mã sách không tồn tại");
            else{
                LabelThongTin.setText(sach.toString());
            }
        }
    }
